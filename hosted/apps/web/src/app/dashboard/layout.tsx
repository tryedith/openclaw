import { createClient } from "@/lib/supabase/server";
import { redirect } from "next/navigation";
import Link from "next/link";
import { UserMenu } from "@/components/UserMenu";

export default async function DashboardLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const supabase = await createClient();
  // Use getSession() instead of getUser() - reads from cookie without network call
  // Middleware already validates and protects the route
  const {
    data: { session },
  } = await supabase.auth.getSession();

  if (!session?.user) {
    redirect("/login");
  }

  const user = session.user;
  const initials = user.email?.[0].toUpperCase() || "?";
  const displayName = user.user_metadata?.full_name || user.email?.split("@")[0] || "User";

  return (
    <div className="min-h-screen flex bg-background">
      {/* Sidebar */}
      <aside className="w-72 bg-background-secondary border-r border-border flex flex-col">
        {/* Logo */}
        <div className="p-6 border-b border-border">
          <Link href="/dashboard" className="flex items-center gap-3">
            <div className="w-10 h-10 rounded-xl bg-primary flex items-center justify-center">
              <svg
                className="w-6 h-6 text-white"
                fill="none"
                viewBox="0 0 24 24"
                stroke="currentColor"
                strokeWidth={2}
              >
                <path
                  strokeLinecap="round"
                  strokeLinejoin="round"
                  d="M8 12h.01M12 12h.01M16 12h.01M21 12c0 4.418-4.03 8-9 8a9.863 9.863 0 01-4.255-.949L3 20l1.395-3.72C3.512 15.042 3 13.574 3 12c0-4.418 4.03-8 9-8s9 3.582 9 8z"
                />
              </svg>
            </div>
            <span className="text-xl font-bold text-foreground">OpenClaw</span>
          </Link>
        </div>

        {/* Navigation */}
        <nav className="flex-1 p-4">
          <div className="space-y-1">
            <NavLink href="/dashboard" icon={<DashboardIcon />}>
              Dashboard
            </NavLink>
            <NavLink href="/dashboard/channels" icon={<ChannelsIcon />}>
              Channels
            </NavLink>
            <NavLink href="/dashboard/debug" icon={<DebugIcon />}>
              Debug
            </NavLink>
            <NavLink href="/dashboard/settings" icon={<SettingsIcon />}>
              Settings
            </NavLink>
          </div>

          {/* Help section */}
          <div className="mt-8 pt-6 border-t border-border">
            <p className="px-3 text-xs font-medium text-foreground-subtle uppercase tracking-wider mb-3">
              Help
            </p>
            <div className="space-y-1">
              <NavLink href="https://docs.openclaw.ai" icon={<DocsIcon />} external>
                Documentation
              </NavLink>
              <NavLink href="https://github.com/openclaw/openclaw" icon={<GitHubIcon />} external>
                GitHub
              </NavLink>
            </div>
          </div>
        </nav>

        {/* User section */}
        <div className="p-4 border-t border-border">
          <UserMenu
            email={user.email || ""}
            displayName={displayName}
            initials={initials}
          />
        </div>
      </aside>

      {/* Main content */}
      <main className="flex-1 overflow-auto">
        <div className="p-8">{children}</div>
      </main>
    </div>
  );
}

function NavLink({
  href,
  icon,
  children,
  external,
}: {
  href: string;
  icon: React.ReactNode;
  children: React.ReactNode;
  external?: boolean;
}) {
  const baseClasses =
    "flex items-center gap-3 px-3 py-2.5 rounded-xl text-sm font-medium transition-colors";
  const defaultClasses = "text-foreground-muted hover:text-foreground hover:bg-background";

  if (external) {
    return (
      <a
        href={href}
        target="_blank"
        rel="noopener noreferrer"
        className={`${baseClasses} ${defaultClasses}`}
      >
        <span className="w-5 h-5">{icon}</span>
        {children}
        <svg
          className="w-3 h-3 ml-auto text-foreground-subtle"
          fill="none"
          viewBox="0 0 24 24"
          stroke="currentColor"
          strokeWidth={2}
        >
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            d="M4.5 19.5l15-15m0 0H8.25m11.25 0v11.25"
          />
        </svg>
      </a>
    );
  }

  return (
    <Link href={href} className={`${baseClasses} ${defaultClasses}`}>
      <span className="w-5 h-5">{icon}</span>
      {children}
    </Link>
  );
}

